package Lecture4Operators;

public class IncDecOperators {
    public static void main(String[] args) {
        // increment decrement operators

        int a = 5;
        System.out.println(a);
        a++;
        System.out.println(a);
        ++a;
        System.out.println(a);

        int b = 5;
        b--;
        System.out.println(b);

    }
}

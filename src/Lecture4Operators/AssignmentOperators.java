package Lecture4Operators;

public class AssignmentOperators {
    public static void main(String[] args) {
        //assignment operators
        int a=20;
        System.out.println(a);
        a+=10; //a=20 ans is a=20+10=30
        System.out.println(a);
        a=a+10;
        System.out.println(a);

    }
}
